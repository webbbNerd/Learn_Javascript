//* The charAt() method of String values returns a new string
// charAt(index)

const sentence = "The quick brown fox jumps over the lazy dog.";
console.log(sentence.charAt(2));

//* The charCodeAt() method of String values returns an integer between 0 and 65535

const str = "AZaz";
console.log(str.charCodeAt(0)); //65
console.log(str.charCodeAt(1)); //90
console.log(str.charCodeAt(2)); //97
console.log(str.charCodeAt(3)); //122
